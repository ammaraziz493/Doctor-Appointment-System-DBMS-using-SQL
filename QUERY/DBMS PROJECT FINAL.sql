-- Create Database
CREATE DATABASE Online_Doctor_AppointmentSystem
-- Use database
USE Online_Doctor_AppointmentSystem
-- create Table
CREATE TABLE HOSPITAL (
    HOSPITAL_ID INT NOT NULL PRIMARY KEY,
    HOSPITAL_Name VARCHAR(50),
    CITY VARCHAR(20),
	COUNTRY VARCHAR(20),
);
PRINT 'SUCCESSFULLY HOSPITAL TABLE CREATED'



-- CREATE DOCTOR TABLE
CREATE TABLE DOCTOR (
DOCTOR_ID INT NOT NULL PRIMARY KEY,
HOSPITAL_ID INT NOT NULL,
FIRST_NAME VARCHAR(20),
LAST_NAME  VARCHAR(20),
DOCTOR_SPECIALIST VARCHAR (30),
PHONE_NUMBER BIGINT,
CONSTRAINT FK_HOSPITAL_DOCTOR FOREIGN KEY (HOSPITAL_ID) REFERENCES HOSPITAL(HOSPITAL_ID)
);
PRINT 'SUCCESSFULY DOCTOR TABLE CREATED'
-- CREATE APPOINTMENT TABLE
CREATE TABLE APPOINTMENT (
APPOINTMENT_ID INT NOT NULL PRIMARY KEY,
DOCTOR_ID INT NOT NULL,
PATIENT_ID INT NOT NULL,
APPOINTMENT_REASON VARCHAR(20),
APPOINTMENT_NUMBER  INT,
APPOINTMENT_DATE DATE,
CONSTRAINT FK_APPPOINTMENT_DOCTOR FOREIGN KEY(DOCTOR_ID) REFERENCES DOCTOR(DOCTOR_ID),
CONSTRAINT FK_APPPOINTMENT_PATIENT FOREIGN KEY(PATIENT_ID) REFERENCES PATIENT(PATIENT_ID)
);
PRINT 'SUCCESSFULY APPOINTMENT TABLE CREATED'
-- CREATE RECEIPT TABLE
CREATE TABLE RECEIPT (
RECEIPT_NUMBER INT NOT NULL PRIMARY KEY ,
APPOINTMENT_ID INT NOT NULL,
RECEIPT_STATUS VARCHAR(10),
CONSTRAINT FK_RECEIPT_APPOINTMENT FOREIGN KEY (APPOINTMENT_ID) REFERENCES APPOINTMENT(APPOINTMENT_ID)
);
PRINT 'SUCCESSFULY RECEIPT TABLE CREATED'

-- CREATE PATIENT TABLE
CREATE TABLE PATIENT (
 PATIENT_ID INT NOT NULL PRIMARY KEY ,
 FIRST_NAME VARCHAR(20),
 LAST_NAME VARCHAR(20),
 PHONE_NUMBER BIGINT ,
 CITY VARCHAR(20),
 COUNTRY VARCHAR(20),
);
PRINT 'SUCCESSFULLY  PATIENT TABLE CREATED'
-- CREATE PAYMENT TABLE
CREATE TABLE PAYMENT (
 PAYMENT_ID INT NOT NULL PRIMARY KEY ,
 RECEIPT_NUMBER INT,
 PATIENT_ID INT,
 PAYMENT_METHOD VARCHAR(20),
 PAYMENT_AMOUNT INT ,
 PAYMENT_DATE DATE,
 CONSTRAINT FK_RECEIPT_PAYMENT FOREIGN KEY (RECEIPT_NUMBER) REFERENCES RECEIPT(RECEIPT_NUMBER),
 CONSTRAINT FK_PATEINT_PAYMENT FOREIGN KEY (PATIENT_ID) REFERENCES PATIENT(PATIENT_ID)
 
);
PRINT 'SUCCESSFULLY  PAYMENT TABLE CREATED'
